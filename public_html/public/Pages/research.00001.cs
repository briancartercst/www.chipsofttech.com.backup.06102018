			<h2 class="title bg4"><a name="development">SNITTER was Created!</a></h2>
			<div class="content">
				<p><strong>SNITTER</strong> is a Sensor Network Investigation Toolkit 
				To Enable Research. With so many COTS sensors available, SNITTER 
				is a toolkit that integrates the sensor readings into a standard 
				RSS format. </p>
				<p>&nbsp;</p>
				<p>
				<a href="papers/Message Transformation Services for Wireless Sensor Networks (MTS-WSN).pdf" target="_blank">
				<strong>Message Transformation Services</strong></a>, a key component 
				of SNITTER, was published and presented at the <a href="http://www.worldacademyofscience.org/worldcomp06/ws/Program/icw28" target="_blank">2006 World Congress 
				in Computer Science, Computer Engineering, and Applied Computing 
				(WORLDCOMP)</a>. </p>
				<p>&nbsp;</p>
				<p>WORLDCOMP is the largest annual gathering of researchers in computer 
				science, computer engineering and applied computing. </p>
