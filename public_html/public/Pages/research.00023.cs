	<h2 class="title bg6"><a name="development">Sensor Deployment Problem!</a></h2>
	<div class="content">
		<p>Existing approaches for determining the optimal deployment positions 
		of sensors, referred to as the <strong>Sensor Deployment Problem (SDP)</strong>, 
		suffer from a number of critical drawbacks. First, homogeneous deployment 
		models have been commonly assumed, but in practice deployments of heterogenous 
		sensors are typical. Second, existing approaches assume isotropic sensing 
		ranges but it has been found that hardware and environmental conditions 
		cause imperfections in sensing. Third, existing models are very application-dependent.
		</p>
		<p>&nbsp;</p>
		<p>
		We propose an extensible modeling framework for the problem of determining 
		optimal deployment positions for a set of heterogeneous, nonisotropic sensors 
		to cover a set of points in an area. The problem is formulated using a genetic 
		algorithm where the objective is to minimize the cost to cover all points. 
		Our technique is to decouple the coverage determination method from the 
		sensor deployment model. This allows the sensor deployment model to remain 
		consistent and address the critical drawbacks of previous models. A homeland 
		security application is presented to illustrate the capabilities of our 
		approach.</p>
		<p>&nbsp;</p>
		<p>The <strong>SDP</strong> has received a great deal of focus in sensor 
		network research. 

		We presented a probabilistic extension to our model at the <a href="http://www.sensorapps.org/" 		target="_blank">IEEE Sensors Applications Symposium (SAS)</a> 2009 titled:  <a href="papers/Carter_SAS09.pdf" 		target="_blank"> A Probabilistic Model for the Deployment of Sensors</a> as an extension to our foundation 		model presented at  SAS 2008 (<a href="papers/Carter_SAS08.pdf" target="_blank">An Extensible Model for the Deployment of Non-Isotropic Sensors</a>).
		</p>
